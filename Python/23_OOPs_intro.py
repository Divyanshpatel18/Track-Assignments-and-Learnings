# Object-Oriented Programming (OOP) is a programming paradigm that uses objects and classes to organize code. 
# Python is an object-oriented programming language that supports OOP principles. 
# Here are the key concepts of OOP in Python:

# Classes and Objects:

# A class is a blueprint for creating objects. It defines the properties (attributes) and behaviors (methods) that objects of the class will have.
# An object is an instance of a class. It represents a specific entity in your program and has its own unique state (attributes) and behavior (methods).

# Encapsulation:
# Encapsulation refers to the bundling of data (attributes) and methods that operate on the data within a single unit (class).
# It hides the internal state of an object from the outside world and only exposes a controlled interface to interact with the object.
# Inheritance:

# Inheritance allows a class (subclass) to inherit properties and behaviors from another class (superclass).
# The subclass can extend or modify the functionality of the superclass while inheriting its attributes and methods.
# Polymorphism:

# Polymorphism allows objects of different classes to be treated as objects of a common superclass.
# It enables you to use a single interface to represent different types of objects and provide different implementations based on the object's type.
# Abstraction:

# Abstraction focuses on hiding the complex implementation details and showing only the essential features of an object.
# # It allows you to create abstract classes with abstract methods that define the interface without specifying the implementation.